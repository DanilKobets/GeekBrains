// Задача: Написать программу, которая из имеющегося массива строк формирует новый массив из строк, длина которых меньше, либо равна 3 символам. Первоначальный массив можно ввести с клавиатуры, либо задать на старте выполнения алгоритма. При решении не рекомендуется пользоваться коллекциями, лучше обойтись исключительно массивами.

// Примеры:
// [“Hello”, “2”, “world”, “:-)”] → [“2”, “:-)”]
// [“1234”, “1567”, “-2”, “computer science”] → [“-2”]
// [“Russia”, “Denmark”, “Kazan”] → []


using System;

class MainClass
{
    public static void Main(string[] args)
    {
        // Ввод исходного массива с клавиатуры
        Console.WriteLine("Введите элементы массива через пробел:");
        string[] inputArray = Console.ReadLine().Split(' ');

        // Создание нового массива для хранения строк, длина которых <= 3
        string[] outputArray = new string[inputArray.Length];
        int outputIndex = 0;

        // Перебор всех строк в исходном массиве
        for (int i = 0; i < inputArray.Length; i++)
        {
            // Проверка длины строки
            if (inputArray[i].Length <= 3)
            {
                // Если длина строки <= 3, добавляем ее в новый массив
                outputArray[outputIndex] = inputArray[i];
                outputIndex++;
            }
        }

        // Создание и вывод финального массива
        string[] finalArray = new string[outputIndex];
        Array.Copy(outputArray, finalArray, outputIndex);
        Console.WriteLine("Финальный массив:");
        Console.WriteLine(string.Join(" ", finalArray));
    }
}
